package ex1;

/**
 * Classe qui propose des operations
 * 
 * @author guewe
 *
 */
public class Calcul {
	
	/**
	 * Retourne une addition
	 * @param a
	 * @param b
	 * @return
	 */
	public int addition(int a, int b) {
		return a + b;
	}
	
	/**
	 * Retourne une soustraction
	 * @param a
	 * @param b
	 * @return
	 */
	public int soustraction(int a, int b) {
		return a - b;
	}
}
